n = input('Digite algo: ')

print('O tipo primitivo deste valor é: ',type(n))

print()

print('Está em maiúsculas?', n.isupper())
print('É um número?', n.isalnum())
print('É alfabético?', n.isalpha())
print('É numérico?', n.isnumeric())
print('É um ASCII?', n.isascii())
print('É um decimal?', n.isdecimal())
print('É um digito?', n.isdigit())
print('É um identificador?', n.isidentifier())
print('Está em minúsculas?', n.islower())
print('É possivel imprimir?', n.isprintable())
print('É um espaço?', n.isspace())
print('É um título?', n.istitle())
